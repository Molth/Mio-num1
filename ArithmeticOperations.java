package a2;

public class ArithmeticOperations {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		int result1 = 0;
		int result2 = 0;
		double result3 = 0.0;
		result1 = num1 + num2;
		result2 = num1 * num2;
		result3 = num1 / num2;
		System.out.println(result1);
		System.out.println(result2);
		System.out.println(result3);
	}
}