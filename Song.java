package a5;

public class Song {
	public static void main(String[] args) {
		int a = 10;
		while (a >= 2) {
			System.out.println(a + " in a bed and the little one said,\r\n" + "'Roll over, roll over'\r\n"
					+ "They all rolled over and one fell out,");
			a--;
		}
		System.out.println("1 in a bed and the little one said,\r\n" + "'Alone at last'");
	}
}